		public static function createMenuChoiceGroupColumn($column_name) {
			Schema::table('menu_items', function (Blueprint $table) use ($column_name) {
				$table->string('choices_'.$column_name, 50)->nullable()->after('menu_subcategories_id');
				$table->string('choices_sku'.$column_name, 50)->nullable()->after('menu_subcategories_id');
			});

			Schema::table('menu_item_approvals', function (Blueprint $table) use ($column_name) {
				$table->string('choices_'.$column_name, 50)->nullable()->after('menu_subcategories_id');
				$table->string('choices_sku'.$column_name, 50)->nullable()->after('menu_subcategories_id');
			});
