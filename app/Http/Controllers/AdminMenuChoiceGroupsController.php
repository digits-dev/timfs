	        //Your code here
	        if($button_name == 'set_status_ACTIVE') {
				DB::table('menu_choice_groups')->whereIn('id',$id_selected)->update([
					'status'=>'ACTIVE', 
					'updated_at' => date('Y-m-d H:i:s'), 
					'updated_by' => CRUDBooster::myId()
				]);
			}
